from collections import deque


class Solution:
    def updateMatrix(self, matrix):
        """
        :type matrix: List[List[int]]
        :rtype: List[List[int]]
        """
        ans = [[0 for y in range(len(matrix[x]))] for x in range(len(matrix))]

        def bfs(i, j):
            memo = {(i, j): True}
            q = deque([(i, j, 0)])
            while q:
                (i, j, c) = q.popleft()
                if matrix[i][j] == 0:
                    return c
                if i > 0 and (i - 1, j) not in memo:
                    memo[(i - 1, j)] = True
                    q.append((i - 1, j, c + 1))
                if i < len(matrix) - 1 and (i + 1, j) not in memo:
                    memo[(i + 1, j)] = True
                    q.append((i + 1, j, c + 1))
                if j > 0 and (i, j - 1) not in memo:
                    memo[(i, j - 1)] = True
                    q.append((i, j - 1, c + 1))
                if j < len(matrix[i]) - 1 and (i, j + 1) not in memo:
                    memo[(i, j + 1)] = True
                    q.append((i, j + 1, c + 1))

        for i in range(len(matrix)):
            for j in range(len(matrix[i])):
                if matrix[i][j] == 1:
                    ans[i][j] = bfs(i, j)
        return ans


